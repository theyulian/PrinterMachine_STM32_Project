void CDC_Receive_Callback(uint8_t* Buf, uint32_t Len)
{



	if(Buf[0]=='a'){
		enviarPro();
	}
	else{
		memcpy(arr,Buf,Len);
		if(arr[0]=='2'){
			memset(tamaux,0,5);
			tamaux[0]=arr[1];
			tamaux[1]=arr[2];
			tamaux[2]=arr[3];
			tam=atoi(tamaux);
			if(arr[tam-1]=='3'){
				memset(arrdat,0,32);
				memset(arrpar,0,32);

				for(i=4;i<((tam-1-4)/2)+4;i++){
					arrdat[i-4]=arr[i];
				}
				aux=0;
				for(i=((tam-1-4)/2)+4;i<tam-1;i++){
					arrpar[aux]=arr[i];
					aux++;
				}
				valorPWM=atoi(arrdat);
				paridad=atoi(arrpar);
				if(paridad==valorPWM+5){
					TIM2->CCR1=valorPWM;
				}


			}
		}
		memset(arr,0,32);
	}

}
